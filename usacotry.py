def low_high(num,nums):
    for i in range(num):
        for x in range(i + 1, num):
            if nums[i] > nums[x]:
                nums[i], nums[x] = nums[x], nums[i] 
    return nums

def high_low(num,nums):
    for i in range(num):
        for x in range(i + 1,num):
            if nums[i] < nums[x]:
                nums[i], nums[x] = nums[x], nums[i] 
    return nums

