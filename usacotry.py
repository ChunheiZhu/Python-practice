def solve13():
    R, C = map(int, input().split())
    grid = [list(map(int, input().split())) for _ in range(R)]

    stack = [(1, 1)]
    visited = set([(1, 1)])

    while stack:
        r, c = stack.pop()
        if (r, c) == (R, C):
            print("yes")
            return

        v = grid[r - 1][c - 1]

        i = 1
        while i * i <= v:
            if v % i == 0:
                r1, c1 = i, v // i
                if 1 <= r1 <= R and 1 <= c1 <= C and (r1, c1) not in visited:
                    visited.add((r1, c1))
                    stack.append((r1, c1))

                r2, c2 = v // i, i
                if (r2, c2) != (r1, c1):
                    if 1 <= r2 <= R and 1 <= c2 <= C and (r2, c2) not in visited:
                        visited.add((r2, c2))
                        stack.append((r2, c2))
            i += 1

    print("no")

