    maxabsx = max(barn[0],lake[0])
    minabsx = min(barn[0],lake[0])
    maxabsy = max(barn[1],lake[1])
    minabsy = min(barn[1],lake[1])
    an = (maxabsx - minabsx) + (maxabsy - minabsy) - 1
    return an
            
