def low_high_nums(num,nums):
    for a in range(num):
        for b in range(num-1):
            if nums[b] > nums[b+1]:
                nums[b],nums[b+1] = nums[b+1],nums[b]
    return nums
def found(num,nums):
    l = 0
    ans = 0
    newnums = low_high_nums(num,nums)
    for i in range(num):
        while newnums[i] - newnums[l] > 5:
                l += 1
        ans = max(ans,i - l + 1)
    return ans

