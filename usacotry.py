    n = int(input())
    must = [input().split() for _ in range(n)]
    a = int(input())
    cannot = [input().split() for _ in range(a)]
    c = int(input())
    group = [input().split() for _ in range(c)]

    group_of = {}

    for gid, members in enumerate(group):
        for name in members:
            group_of[name] = gid

    violations = 0

    for pair in must:
        a = pair[0]
        b = pair[1]
        if group_of[a] != group_of[b]:
            violations = violations + 1

    for pair in cannot:
        a = pair[0]
        b = pair[1]
        if group_of[a] == group_of[b]:
            violations = violations + 1

    print(violations)

