def mat90(num,mat):
    n = num
    for _ in range(4):
        if check(n,mat):
            return mat
        nmat = []           
        for row in zip(*mat[::-1]):    
            nmat.append(list(row))  
        mat = nmat
    return mat                   

def check(num,mat):
    n = num
    for i in range(n):
        for x in range(n-1):
            if mat[i][x] > mat[i][x+1]:
                return False
    for i in range(n):
        for x in range(n-1):
            if mat[x][i] > mat[x+1][i]:
                return False
    return True

