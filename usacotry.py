def always_before(i, j, pos, K):
    # æ£€æŸ¥ç‰› i æ˜¯å¦å§‹ç»ˆåœ¨ç‰› j å‰é¢
    for r in range(K):
        if pos[r][i] >= pos[r][j]:
            return False
    return True

