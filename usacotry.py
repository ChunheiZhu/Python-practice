def _area(rect):
    """è®¡ç®—çŸ©å½¢é¢ç§¯ rect = (x1, y1, x2, y2)"""
    x1, y1, x2, y2 = rect
    w = max(0, x2 - x1)   # å®½åº¦
    h = max(0, y2 - y1)   # é«˜åº¦
    return w * h

def _overlap(a, b):
    """è®¡ç®—ä¸¤ä¸ªçŸ©å½¢ a, b çš„é‡å é¢ç§¯"""
    ax1, ay1, ax2, ay2 = a
    bx1, by1, bx2, by2 = b
    # x æ–¹å‘é‡å 
    w = max(0, min(ax2, bx2) - max(ax1, bx1))
    # y æ–¹å‘é‡å 
    h = max(0, min(ay2, by2) - max(ay1, by1))
    return w * h

def solve_blocked_billboard():
    # è¾“å…¥å¹¿å‘Šç‰Œ Aã€B å’Œå¡è½¦ T
    print('Enter Test Data To 1.Blocked Billboard')
    ax1, ay1, ax2, ay2 = map(int, input().split())
    bx1, by1, bx2, by2 = map(int, input().split())
    tx1, ty1, tx2, ty2 = map(int, input().split())

    A = (ax1, ay1, ax2, ay2)
    B = (bx1, by1, bx2, by2)
    T = (tx1, ty1, tx2, ty2)

    # A å’Œ B å„è‡ªçš„å¯è§é¢ç§¯
    visible_A = _area(A) - _overlap(A, T)
    visible_B = _area(B) - _overlap(B, T)

    # è¾“å‡ºç»“æœ
    print(visible_A + visible_B)

